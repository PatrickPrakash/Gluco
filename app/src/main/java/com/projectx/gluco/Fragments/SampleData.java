public class SampleData {
    private float valueX;
    private float valueY;

    public SampleData(float valueX, float valueY) {
        this.valueX = valueX;
        this.valueY = valueY;
    }

    public float getValueX() {
        return valueX;
    }

    public float getValueY() {
        return valueY;
    }
}
